// ── Sprite caching ──
function cacheSprite(key, pixelData, scale) {
    if (!SpriteCache[key]) {
        SpriteCache[key] = createSpriteCanvas(pixelData, scale || 2);
    }
    return SpriteCache[key];
}

function getCharSprite(charId, frameIdx) {
    const key = `char_${charId}_${frameIdx}`;
    if (!SpriteCache[key]) {
        const data = CHAR_SPRITES[charId];
        if (!data) return null;
        const frame = data.frames[frameIdx] || data.frames[0];
        SpriteCache[key] = createSpriteCanvas(frame, 2);
    }
    return SpriteCache[key];
}

function getEnemySprite(type, frameIdx) {
    const frames = ENEMY_SPRITES[type];
    if (!frames) return null;
    const key = `enemy_${type}_${frameIdx % frames.length}`;
    if (!SpriteCache[key]) {
        SpriteCache[key] = createSpriteCanvas(frames[frameIdx % frames.length], 2);
    }
    return SpriteCache[key];
}

function getItemSprite(type) {
    const key = `item_${type}`;
    if (!SpriteCache[key] && ITEM_SPRITES[type]) {
        SpriteCache[key] = createSpriteCanvas(ITEM_SPRITES[type], 3);
    }
    return SpriteCache[key];
}

// Pre-cache all sprites on load
function initSprites() {
    for (const charId in CHAR_SPRITES) {
        for (let i = 0; i < CHAR_SPRITES[charId].frames.length; i++) {
            getCharSprite(charId, i);
        }
    }
    for (const type in ENEMY_SPRITES) {
        const frames = ENEMY_SPRITES[type];
        for (let i = 0; i < frames.length; i++) {
            getEnemySprite(type, i);
        }
    }
    for (const type in ITEM_SPRITES) {
        getItemSprite(type);
    }
}
